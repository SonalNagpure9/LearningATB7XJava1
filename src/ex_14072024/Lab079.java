package ex_14072024;

public class Lab079 {
    public static void main(String[] args) {
        //Ternary Operator
        //? if true  do this : else do that
        int result  = 10>40  ?  89 : 91;
        System.out.println(result);

        int a1 = (30>40) ? 10:20;
        System.out.println(a1);

        int a2 = (56>40) ? 20 : 30;
        System.out.println(a2);
    }
}
